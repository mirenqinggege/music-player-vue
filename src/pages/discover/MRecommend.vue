import {getBannerStore} from '@/store'
import MGroup from '@/components/group/MGroup.vue'

const bannerStore = getBannerStore()

const bannerList: ComputedRef<BannerItem[]> = computed(() => bannerStore.getBannerList)

const mBanner = ref()

onMounted(() => {
